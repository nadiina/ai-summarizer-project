def contents_prompt(document_text: str = "Текст для формування змісту не надійшов, проінформуй про це") -> list:
    return [
        {
            "role": "system",
            "content": (
                "Ти — досвідчений аналітик текстів і редактор. "
                "Твоя задача — побудувати зміст текстового документа та для кожного пункту змісту надати ключові тези або цитати. "
                "Пункти повинні бути логічними розділами документа. "
                "Не використовуй форматування (жирний, курсив тощо)."
            )
        },
        {
            "role": "user",
            "content": (
                "Проаналізуй наступний документ:\n\n"
                f"{document_text}\n\n"
                "1. Побудуй зміст (логічні розділи тексту).\n"
                "2. Для кожного розділу наведи 2–3 ключові тези або цитати з відповідної частини тексту.\n"
                "3. Якщо розділ має підрозділи, також врахуй їх у структурі.\n"
                "4. Результат подай у вигляді списку:\n"
                "   1. Назва розділу\n"
                "       - ключова теза або цитата\n"
                "       - ...\n\n"
                "Ігноруй пошкоджені символи (наприклад «�») і віднови сенс по можливості."
            )
        }
    ]
