def summary_prompt(document_text: str = "Текст не надійшов, напиши про це") -> list:
    messages = [
        {
            "role": "system",
            "content": (
                "Ти — досвідчений аналітик текстів із навичками стислого викладу. "
                "Твоя задача — виділити та представити ключові ідеї документа максимально зрозуміло, "
                "без будь‑якого форматування (жирний, курсив тощо)."
            )
        },
        {
            "role": "user",
            "content": (
                "Сформуй короткий підсумок для наступного текстового документа:\n\n"
                f"{document_text}\n\n"
                "Примітка: текст може містити артефакти кодування (наприклад «�»). "
                "Проаналізуй та віднови сенс, ігноруючи пошкоджені символи, "
                "та дай лаконічний виклад основних ідей."
            )
        }
    ]
    return messages
